import Foundation
import CoreGraphics

extension Int {
    
    var cgFloat: CGFloat { return CGFloat(self) }
    var double: Double { return Double(self) }
    var string: String { return String(self) }
    var int64: Int64 { return Int64(self) }
    var float: Float { return Float(self) }
}
